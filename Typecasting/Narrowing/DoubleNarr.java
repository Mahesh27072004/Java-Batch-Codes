class DoubleNarr
{
	public static void main(String[] args)
	{
		double a = 124544.05464842;
		float b = (float) a;
		System.out.println("to float: "+b);
		long c = (long) a;
		System.out.println("to long: "+c);
		int d = (int) a;
		System.out.println("to int: "+d);
		char ch = (char) a;
		System.out.println("to char: "+ch);
		short e = (short) a;
		System.out.println("to short : "+e);
		byte f = (byte) a;
		System.out.println("to byte: "+f);
	}
}
