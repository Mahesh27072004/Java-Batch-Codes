class LongNarr
{
	public static void main(String[] args)
	{
		long a = 52867546548l;
		int d = (int) a;
		System.out.println("to int: "+d);
		char ch = (char) a;
		System.out.println("to char: "+ch);
		short e = (short) a;
		System.out.println("to short : "+e);
		byte f = (byte) a;
		System.out.println("to byte: "+f);
	}
}
