class ShortWidening
{
	public static void main(String[] args)
	{
		short a = 35;
		//char d = a;
		//System.out.println("to char : "+d);//CTE
		int b = a;
		System.out.println("to int : "+b);
		long c = a;
		System.out.println("to long : "+c);
		float e = a;
		System.out.println("to float : "+e);
		double f = a;
		System.out.println("to double : "+f);
	}
}
		